local function join_arrays(arr1, arr2)
  local result = {}

  -- Insert elements from the first array
  for _, value in ipairs(arr1) do
    table.insert(result, value)
  end

  -- Insert elements from the second array
  for _, value in ipairs(arr2) do
    table.insert(result, value)
  end

  return result
end

